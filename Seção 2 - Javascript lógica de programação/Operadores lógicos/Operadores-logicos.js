/*
Operadores Lógicos

&& -> And -> E
|| -> OU -> OU
! -> NOT -> NÃO
*/

console.log('And = ',true && true) // retorna verdadeiro
console.log('And = ',true && false) //  retorna falso

console.log('OU =', true || true) //  retorna verdadeiro
console.log('OU =', true || false) //  retorna verdadeiro
console.log('OU =', false || false) //  retorna falso
 
console.log('NOT = ', !true) //  retorna falso
console.log('NOT = ', !false) //  retorna verdadeiro